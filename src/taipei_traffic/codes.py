def vehicle_category(code: str | None) -> str | None:
    """將車種代碼歸為大類，如 C03 → 機車。"""
    if not isinstance(code, str) or len(code) < 2:
        return None
    return _CATEGORY_BY_PREFIX.get(code[:2], "其他車")


